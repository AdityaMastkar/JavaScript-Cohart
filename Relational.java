public class Relational {
    public static void main (String [] args) {
        int a = 20;
        int b = 20;
        System.out.println((a!=b)); //not equal
        System.out.println((a==b)); // equal to
        System.out.println((a>b));  //grater than
        System.out.println((a<b));  //less than
        System.out.println((a>=b)); //grater than equal
        System.out.println((a<=b)); //less than equal


    }
}
