public class Switch {
    public static void main (String [] args) {
        char Num = 'a';
        // int num = 1;
        switch (Num) {
            case 'a' : System.out.println("Brother");
            break;
            case 'b' : System.out.println("Mother");
            break;
            case 'c' : System.out.println("Frontman");
            break;
            default : System.out.println("Anath");

        }

    }
}
