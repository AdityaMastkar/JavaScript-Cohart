public class DataTypes {
    public static void main (String [] args) {
        byte b = 5;
        System.out.println(b);
        char a = 'a';
        System.out.println(a);
        boolean var = true;
        System.out.println(var);
        float price = 10.5f;
        short n = 240;
        System.out.println(price);
        System.out.println(n);

    }
}
